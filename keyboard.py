def release_key(keyp):
    hexKeyCode = gen_hex_from_key(str.upper(keyp))
    extra = ctypes.c_ulong(0)
    ii_ = Input_I()
    ii_.ki = KeyBdInput(0, hexKeyCode, 0x0008 | 0x0002, 0,
                        ctypes.pointer(extra))
    x = Input(ctypes.c_ulong(1), ii_)
    ctypes.windll.user32.SendInput(1, ctypes.pointer(x), ctypes.sizeof(x))

#Pressiona e solta tecla
def press(hexKeyCode, delay=0.5):
    key_down(hexKeyCode)
    time.sleep(delay)
    release_key(hexKeyCode)

#Transforma string em hex
def gen_hex_from_key(key):
    for i, elemento in enumerate(keylist):
        if elemento == key:
            return hexlist[i]
    return None